pub unsafe extern "C" fn apply_seqtbl_patches() {
    // FIX_WALL_BUMP_TRIGGERS_TILE_BELOW: a wall bump should not flag the
    // character as bumped (which triggers the tile below). Switch the action
    // byte at bumpfall+1 from actions_5_bumped (5) to actions_3_in_midair (3).
    if (*fixes).fix_wall_bump_triggers_tile_below != 0 {
        let idx = (BUMPFALL + 1 - SEQTBL_BASE) as usize;
        seqtbl[idx] = 3;
    }
}

/// Debug stub. The real validator (which compares `seqtbl` against the embedded
