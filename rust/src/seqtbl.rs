    /// Total array length matches base + USE_TELEPORTS extension.
    #[test]
    fn seqtbl_length_is_correct() {
        unsafe {
            assert_eq!(seqtbl.len(), 2310 + 45);
        }
    }

    /// First few bytes of the running sequence — the simplest spot check that
    /// the byte data starts where we expect.
    /// Sequence at offset 0 (running): act(actions_1_run_jump), jmp(runcyc1)
    ///   = SEQ_ACTION, 1, SEQ_JMP, 0x81, 0x19  (runcyc1 = 0x1981, little-endian)
    #[test]
    fn seqtbl_first_bytes_encode_running_sequence() {
        unsafe {
            assert_eq!(seqtbl[0], 0xF9, "byte 0: SEQ_ACTION");
            assert_eq!(seqtbl[1], 0x01, "byte 1: actions_1_run_jump");
            assert_eq!(seqtbl[2], 0xFF, "byte 2: SEQ_JMP");
            assert_eq!(seqtbl[3], 0x81, "byte 3: runcyc1 lo");
            assert_eq!(seqtbl[4], 0x19, "byte 4: runcyc1 hi (0x1981)");
        }
    }

    /// All 115 base offsets must match the DOS original exactly.
    #[test]
    fn seqtbl_offsets_match_original_dos_values() {
        for (i, &expected) in ORIGINAL_SEQTBL_OFFSETS.iter().enumerate() {
            assert_eq!(
                seqtbl_offsets[i], expected,
                "seqtbl_offsets[{}] should be {:#06X}, got {:#06X}",
                i, expected, seqtbl_offsets[i],
            );
        }
        // 116th entry is the teleport offset.
        assert_eq!(seqtbl_offsets[115], TELEPORT);
        assert_eq!(seqtbl_offsets[115], 0x2274);
    }

    /// The cumulative `pub const` chain must produce the documented absolute
    /// addresses. Spot-checks on a sampling of labels.
    #[test]
    fn label_constants_are_self_consistent() {
        assert_eq!(RUNNING,          0x196E);
        assert_eq!(STARTRUN,         RUNNING + 5);
        assert_eq!(STARTRUN,         0x1973);
        assert_eq!(STAND,            RUNCYC7 + 11);
        assert_eq!(STAND,            0x19A0);
        assert_eq!(BUMPFALL,         0x1E06);
        assert_eq!(CLIMBSTAIRS_LOOP, CLIMBSTAIRS + 81);
        assert_eq!(CLIMBSTAIRS_LOOP, 0x212E);
        assert_eq!(MCLIMB_LOOP,      0x2270);
        assert_eq!(TELEPORT,         MCLIMB_LOOP + 4);
        assert_eq!(TELEPORT,         0x2274);
        assert_eq!(TELEPORT_LOOP,    TELEPORT + 41);
        assert_eq!(TELEPORT_LOOP,    0x229D);
    }

    /// The teleport extension's terminating jmp must point to `teleport_loop`,
    /// encoded little-endian. A miscount before the loop label would silently
    /// corrupt the animation.
    #[test]
    fn teleport_jmp_points_at_teleport_loop() {
        unsafe {
            // Last 3 bytes of the array are the jmp instruction.
            let last = seqtbl.len();
            assert_eq!(seqtbl[last - 3], 0xFF, "SEQ_JMP");
            let lo = seqtbl[last - 2] as u16;
            let hi = seqtbl[last - 1] as u16;
            let target = lo | (hi << 8);
            assert_eq!(target, TELEPORT_LOOP);
            assert_eq!(target, 0x229D);
        }
    }

    /// With fixes disabled, applying patches must leave seqtbl unchanged.
    #[test]
    fn apply_seqtbl_patches_leaves_seqtbl_unchanged_when_fix_disabled() {
        let idx = (BUMPFALL + 1 - SEQTBL_BASE) as usize;
        unsafe {
            // Reset to base via set_options_to_default → fixes points at
            // fixes_disabled_state (all-zero).
            set_options_to_default();
            // Restore the original byte in case a prior test mutated it.
            seqtbl[idx] = 5;

            apply_seqtbl_patches();

            assert_eq!(seqtbl[idx], 5, "actions_5_bumped (default) preserved");
        }
    }

    /// With fix_wall_bump_triggers_tile_below enabled, the byte must be
    /// rewritten to actions_3_in_midair (3).
    #[test]
    fn apply_seqtbl_patches_writes_correct_byte_when_fix_enabled() {
        let idx = (BUMPFALL + 1 - SEQTBL_BASE) as usize;
        unsafe {
            set_options_to_default();
            // Enable fixes, then flip the specific fix on.
            turn_fixes_and_enhancements_on_off(1);
            fixes_saved.fix_wall_bump_triggers_tile_below = 1;

            // Restore baseline before patching, since other tests may have run.
            seqtbl[idx] = 5;
            apply_seqtbl_patches();

            assert_eq!(seqtbl[idx], 3, "actions_3_in_midair after patch");

            // Cleanup: restore byte and disable fix to avoid leaking state to
            // other tests that read this byte.
            seqtbl[idx] = 5;
            fixes_saved.fix_wall_bump_triggers_tile_below = 0;
            turn_fixes_and_enhancements_on_off(0);
        }
    }
